Welcome to my website!
<b>Guitar</b> is my main instrument. I also play violin, mandolin and bass guitar. 
And <b>I sing</b> (lead and harmonies).
I'm a <b>versatile</b> player, comfortable with a wide range of styles and sounds.  
R&amp;B, funk, blues, rock, jazz, country, bluegrass,... I'm your guy.
<br><br>
I've been playing professionally most of my life, which started in <b>Connecticut</b>. 
I began playing the violin at age 8, then
picked up the guitar at age 13. My early training was in classical violin
and theory. I developed my guitar chops playing in bands on the East coast.
After several years of playing the clubs on <b>New York City's</b>
infamous Bleecker Street, I moved to <b>Los Angeles</b> where I attended 
<a href="http://www.mi.edu" target="_blank"><b>Musicians Institute</b></a>. 
After graduating from Musicians Institute with honors, I worked
around Los Angeles as a studio musician and recording engineer. In 1990, I escaped to <b>Boulder,
Colorado</b>, where I live now.</span>
<br><br>
Since moving to Colorado, I've played with a bunch of different groups - they're listed below,
and I encourage you to visit their web sites. 
Visit my <a href="shows.php">Shows</a> page for a list of past and future live shows.
I've done a lot of studio work (both as a musician and engineer), and some of that work
is listed in my Discography below. My home recording studio can accommodate remote tracking, 
rehearsals and CD pre-production.
<p>
My non-musical interests include long-distance adventure cycling and telemark skiing.
I also run my own computer programming company, 
<a href="http://www.pixelzoom.com" target="_blank">PixelZoom</a>.
<br><br>
After you've had a look around, visit my <a href="contact.php">Contact</a> page.
Let me know what you think about the site.
And if you're a musician who's interested in gigging, recording or songwriting, 
don't hesitate to contact me.
